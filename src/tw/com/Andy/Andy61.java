package tw.com.Andy;

public class Andy61 {

	public static void main(String[] args) {
		double a = -10.123;
		System.out.println(Math.floor(a));
//		floor = 向下取整數  
//		例如10.001~10.999 > 10.0
//		例如-10.001~10.999 > -11.0
		System.out.println(Math.ceil(a));
//		ceil = 向上取整數  
//		例如10.001~10.999 > 11.0
//		例如-10.001~10.999 > -10.0	

	}

}
